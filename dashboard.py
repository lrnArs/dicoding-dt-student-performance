@st.cache_data
def load_pred_data(path: str = "pred_data.csv") -> pd.DataFrame:
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"File not found: {p.resolve()}")
    df = pd.read_csv(p)
    df = normalize_columns(df)
    # Ensure we have Original_Status (the true status from input)
    if "Original_Status" not in df.columns and "Status" in df.columns:
        df["Original_Status"] = df["Status"]
    # Add risk level for enrolled students
    if "Prob_Graduate" in df.columns:
        mask = df["Original_Status"].astype(str).str.strip() == "Enrolled"
        df["Risk_Level"] = ""
        df.loc[mask, "Risk_Level"] = df.loc[mask, "Prob_Graduate"].apply(get_risk_level)
    # Map Course codes to readable names
    if "Course" in df.columns:
        df["Course_Name"] = df["Course"].apply(map_course)
    return df

