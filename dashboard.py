NOTEBOOK_COLUMNS = set(RAW_TO_NOTEBOOK_COLS.values())

def normalize_columns(df: pd.DataFrame) -> pd.DataFrame:
    """Rename columns to notebook names."""
    rename_map = {}
    for col in df.columns:
        key = canonical(col)
        if key in CANONICAL_RAW_MAP:
            rename_map[col] = CANONICAL_RAW_MAP[key]
        elif col in NOTEBOOK_COLUMNS:
            rename_map[col] = col
    return df.rename(columns=rename_map).copy()

def get_risk_level(prob_graduate: float) -> str:
    if prob_graduate >= 0.7:
        return "Low risk"      # Actually low risk of dropout
    elif prob_graduate >= 0.3:
        return "Medium risk"
    else:
        return "High risk"

